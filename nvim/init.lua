local function is_windows()
  return vim.fn.has 'win32' == 1
end

local function get_python_path()
  if is_windows() then
    return vim.fn.stdpath 'config' .. '\\nvim_pyenv\\Scripts\\python.exe'
  end
  return vim.fn.stdpath 'config' .. '/nvim_pyenv/bin/python'
end

vim.g.python3_host_prog = get_python_path()

